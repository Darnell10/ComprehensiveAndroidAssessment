public class Terrier {

    private String terry;
    private String terryImage;

    public Terrier(String terry, String terryImage) {
        this.terry = terry;
        this.terryImage = terryImage;
    }

    public String getTerry() {
        return terry;
    }

    public void setTerry(String terry) {
        this.terry = terry;
    }

    public String getTerryImage() {
        return terryImage;
    }

    public void setTerryImage(String terryImage) {
        this.terryImage = terryImage;
    }
}
